    private void simpanDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanDataActionPerformed
        String nama = namaInput.getText();
        String nim = nimInput.getText();
        String prodi = null;
        
        for(Enumeration iterasi = pilihProdi.getElements() ; iterasi.hasMoreElements();){
            JRadioButton btn = (JRadioButton) iterasi.nextElement();
            if (btn.isSelected()) {
                prodi = btn.getText();
            }
        }
        boolean status = driver.inputNewMahasiswa(mhsCNTRL.inputMahasiswa(nim, nama, prodi));
        
        if (status) {
            statusInput.setText("Data berhasil di input");
        } else {
        statusInput.setText("Data berhasil gagal di infput");
        }
    }//GEN-LAST:event_simpanDataActionPerformed

    private void ListMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListMahasiswaMouseClicked
        String nama = ListMahasiswa.getSelectedValue();
        Mahasiswa newMHS = driver.search(nama);
        if (newMHS != null) {
            nimDisplay.setText(newMHS.getNim());
            namaDisplay.setText(newMHS.getNama());
            prodiDisplay.setText(newMHS.getProdi());
            
            editBTN.setVisible(true);
            hapusBTN.setVisible(true);
        }
    }//GEN-LAST:event_ListMahasiswaMouseClicked

