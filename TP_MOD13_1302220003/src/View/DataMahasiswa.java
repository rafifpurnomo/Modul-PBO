    private void hapusBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBTNActionPerformed
        String nim = nimDisplay.getText();
        boolean status = driver.hapusMahasiswa(nim);
        
        if (status) {
            JOptionPane.showMessageDialog(this, "mahasiswa berhasil di hapus");
            ListMahasiswa.removeAll();
            listModel.removeAllElements();
            loadMahasiswa();
        } else {
            JOptionPane.showMessageDialog(this, "terjadi kesalahan pada server");
        }
    }//GEN-LAST:event_hapusBTNActionPerformed

